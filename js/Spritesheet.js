/*
 * image: Image
 * rows: Integer
 * columns: Integer
 * spriteWidth: Integer
 * spriteHeight: Integer
 */

function Spritesheet(image, rows, columns, spriteWidth, spriteHeight) {

    this.image = image;
    this.rows = rows;
    this.columns = columns;
    this.spriteWidth = spriteWidth;
    this.spriteHeight = spriteHeight;

}
